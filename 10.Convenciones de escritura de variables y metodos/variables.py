# Variables en minúsculas con guiones bajos
nombre_usuario = "Ana Pérez"
edad_usuario = 30
saldo_cuenta = 1500.75

# Variables con nombres descriptivos
numero_de_productos = 50
precio_unitario = 19.99

def imprimir_variables():
    print(f"Nombre de usuario: {nombre_usuario}")
    print(f"Edad: {edad_usuario}")
    print(f"Saldo de cuenta: {saldo_cuenta}")
    print(f"Número de productos: {numero_de_productos}")
    print(f"Precio unitario: {precio_unitario}")

if __name__ == "__main__":
    imprimir_variables()
